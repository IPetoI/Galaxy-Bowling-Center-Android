package com.example.galaxybowlingcentera.AdatModellek;

public class Kepek {

    private String hanyadik;
    private int kep;


    public Kepek() { }

    public Kepek(String hanyadik, int kep) {
        this.hanyadik = hanyadik;
        this.kep = kep;
    }

    public String getHanyadik() {
        return hanyadik;
    }
    public int getKep() {
        return kep;
    }

}
