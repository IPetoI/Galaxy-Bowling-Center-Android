package com.example.galaxybowlingcentera.AdatModellek;

public class IdopontokTelt {
    private String idopontok;
    private int helyek;

    public IdopontokTelt(String idopontok, int helyek) {
        this.idopontok = idopontok;
        this.helyek = helyek;
    }

    public String getIdopontok() {
        return idopontok;
    }

    public int getHelyek() {
        return helyek;
    }
}
